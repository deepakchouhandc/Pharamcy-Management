    private void AddbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddbtnActionPerformed
        // TODO add your handling code here:
        
        String compId = cId.getText();
        String compName = cName.getText();
        String compQty = cQty.getText();
        String compaddress = caddress.getText();
       
        
        try{
            
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("insert into company values(?,?,?,?)");
            ps.setString(1, compId);
            ps.setString(2, compName);
            ps.setString(3, compQty);
            ps.setString(4, compaddress);
           
            ps.executeUpdate();
            
           JOptionPane.showMessageDialog(null, "Company Added Successefully");
           
            
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_AddbtnActionPerformed

