    public void SelectComp()
    {
        
        try {
            // TODO add your handling code here:
            Class.forName("com.mysql.cj.jdbc.Driver");
            try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharma","root","452010")) {
                Statement st = con.createStatement();
                
                String qr ="select * from company";
                java.sql.ResultSet rs=st.executeQuery(qr);
                ResultSetMetaData rsmd = rs.getMetaData();
                DefaultTableModel model =  (DefaultTableModel) compdataTbl.getModel();
                int cols =rsmd.getColumnCount();
                model.setRowCount(0);            
                String ci,cn,cq,ca;
                while(rs.next())
                {
                    ci=rs.getString(1);
                    cn=rs.getString(2);
                    cq=rs.getString(3);
                    ca=rs.getString(4);
                   
                    
                    String[]row={ci,cn,cq,ca};
                    model.addRow(row);
                }
                st.close();
            }
                
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Medicine.class.getName()).log(Level.SEVERE, null, ex);
        } catch (java.sql.SQLException ex) {
            Logger.getLogger(Medicine.class.getName()).log(Level.SEVERE, null, ex);
        }
    }     
    
